# Step 1: Import Required Libraries
# ------------------------------------------------------------
import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score
import matplotlib.pyplot as plt

df = pd.read_csv("crop_production.csv")
print("‚úÖ Dataset loaded successfully!")

print(df.head())
print("\nDataset Info:")
print(df.info())
print("\nTotal Rows:", len(df))

print("\nMissing values in each column:")
print(df.isnull().sum())

df = df.dropna()

# If dataset has columns like 'State', 'Crop', etc. we can encode them
for col in df.select_dtypes(include=['object']).columns:
    df[col] = df[col].astype('category').cat.codes

print("\n‚úÖ Data Cleaned and Encoded Successfully!")
print(df.head())

# Step 5: Define Features and Target
# ------------------------------------------------------------
# Change 'Production' to your actual target column name if different
# Example columns: ['Area', 'Rainfall', 'Fertilizer', 'Production']
target_column = 'Production' if 'Production' in df.columns else df.columns[-1]

X = df.drop(columns=[target_column])
y = df[target_column]

print("\nFeatures used for prediction:")
print(list(X.columns))
print("\nTarget variable:", target_column)

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.25, random_state=42)

model = LinearRegression()
model.fit(X_train, y_train)

y_pred = model.predict(X_test)

mse = mean_squared_error(y_test, y_pred)
r2 = r2_score(y_test, y_pred)

print("\nüìä Model Performance:")
print("Mean Squared Error (MSE):", round(mse, 2))
print("R¬≤ Score:", round(r2, 3))

plt.figure(figsize=(6,5))
plt.scatter(y_test, y_pred, color='green')
plt.xlabel("Actual Crop Production")
plt.ylabel("Predicted Crop Production")
plt.title("Actual vs Predicted Crop Production")
plt.grid(True)
plt.show()

# -------------

new_data = pd.DataFrame({
    'Area': [2500],
    'Rainfall': [400],
    'Fertilizer': [180]
})

